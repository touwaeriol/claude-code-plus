                onStop = { onStop?.invoke() },
                modifier = Modifier.fillMaxWidth()
            )
        }
        
        // Â∑≤ÈÄâÊã©ÁöÑ‰∏ä‰∏ãÊñáÊòæÁ§∫
        if (contexts.isNotEmpty()) {
