                modifier = Modifier.fillMaxWidth()
            ) {
                Row(
                    horizontalArrangement = Arrangement.spacedBy(6.dp)
                ) {
                    contexts.forEach { context ->
                        Box(
                            modifier = Modifier
                                .background(
                                    JewelTheme.globalColors.borders.focused,
                                    RoundedCornerShape(16.dp)
                                )
                                .padding(horizontal = 10.dp, vertical = 4.dp)
                                .clickable { onContextRemove(context) },
                            contentAlignment = Alignment.Center
                        ) {
                            Row(
                                verticalAlignment = Alignment.CenterVertically,
                                horizontalArrangement = Arrangement.spacedBy(4.dp)
                            ) {
                                Text(
                                    when (context) {
                                        is ContextReference.FileReference -> "üìÑ"
                                        is ContextReference.GitReference -> "üîÄ"
                                        else -> "üìé"
                                    },
                                    style = JewelTheme.defaultTextStyle.copy(fontSize = 10.sp)
                                )
                                Text(
                                    when (context) {
                                        is ContextReference.FileReference -> context.path.substringAfterLast('/')
                                        is ContextReference.GitReference -> context.content
                                        else -> "Êú™Áü•"
                                    },
                                    style = JewelTheme.defaultTextStyle.copy(
                                        fontSize = 11.sp,
                                        color = JewelTheme.globalColors.text.normal
                                    )
                                )
                                Text(
                                    "√ó",
                                    style = JewelTheme.defaultTextStyle.copy(
                                        fontSize = 10.sp,
                                        color = JewelTheme.globalColors.text.disabled
                                    )
                                )
                            }
                        }
                    }
                }
            }
        }
        
        // ‰∏ªËæìÂÖ•Ê°ÜÂÆπÂô® - Áªü‰∏ÄËÉåÊôØÔºåÂåÖÂê´ÊâÄÊúâÊéß‰ª∂
        Box(
            modifier = Modifier
                .fillMaxWidth()
                .background(
                    JewelTheme.globalColors.panelBackground, // ‰ΩøÁî®Jewel‰∏ªÈ¢òÁöÑÈù¢ÊùøËÉåÊôØËâ≤
                    RoundedCornerShape(12.dp)
                )
                .padding(12.dp)
        ) {
            Column(
                modifier = Modifier.fillMaxWidth(),
                verticalArrangement = Arrangement.spacedBy(8.dp)
            ) {
                // È°∂ÈÉ®Â∑•ÂÖ∑Ê†èÔºöAdd ContextÊåâÈíÆÔºàÂ∑¶Ôºâ
                Row(
                    modifier = Modifier.fillMaxWidth(),
                    horizontalArrangement = Arrangement.Start,
                    verticalAlignment = Alignment.CenterVertically
                ) {
                    // Add Context ÊåâÈíÆ
                    Box(
                        modifier = Modifier
                            .background(Color.Transparent, RoundedCornerShape(6.dp))
                            .clickable(enabled = enabled && !isGenerating) {
                                // Ê∑ªÂä†‰∏ä‰∏ãÊñáÂäüËÉΩ
                            }
                            .padding(horizontal = 8.dp, vertical = 4.dp),
                        contentAlignment = Alignment.Center
                    ) {
                        Row(
                            verticalAlignment = Alignment.CenterVertically,
                            horizontalArrangement = Arrangement.spacedBy(4.dp)
                        ) {
                            Text("üìé", style = JewelTheme.defaultTextStyle.copy(fontSize = 12.sp))
                            Text(
                                "Add Context",
                                style = JewelTheme.defaultTextStyle.copy(
                                    fontSize = 12.sp,
                                    color = JewelTheme.globalColors.text.disabled
                                )
                            )
                        }
                    }
                }
                
                // ‰∏ªËæìÂÖ•Ê°Ü - ÂéªÊéâËæπÊ°ÜÔºå‰ΩøÁî®ÈÄèÊòéËÉåÊôØ
