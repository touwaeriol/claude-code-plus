@Composable
private fun CompactModelSelector(
    currentModel: AiModel,
    onModelChange: (AiModel) -> Unit,
    enabled: Boolean = true,
    modifier: Modifier = Modifier
) {
    val models = listOf(AiModel.SONNET, AiModel.OPUS)
    val selectedIndex = models.indexOf(currentModel).takeIf { it >= 0 } ?: 0
    var expanded by remember { mutableStateOf(false) }
    
    // ‰ΩøÁî®ÁÆÄÂåñÁöÑÁÇπÂáªÂºèÈÄâÊã©Âô®ÔºåÁ±ª‰ºº‰∏ãÊãâÊ°Ü‰ΩÜÊõ¥Á¥ßÂáë
    Box(modifier = modifier) {
        Text(
            text = "${currentModel.displayName} ‚åÉ",
            style = JewelTheme.defaultTextStyle.copy(
                fontSize = 11.sp,
                color = JewelTheme.globalColors.text.disabled
            ),
            modifier = Modifier
                .clickable(enabled = enabled) {
                    expanded = !expanded
                }
                .padding(horizontal = 4.dp, vertical = 2.dp)
        )
        
        // ÁÆÄÂçïÁöÑ‰∏ãÊãâËèúÂçï
        if (expanded) {
            Column(
                modifier = Modifier
                    .background(
                        JewelTheme.globalColors.panelBackground, // ‰∏é‰∏ªÂÆπÂô®Áõ∏ÂêåÁöÑÈù¢ÊùøËÉåÊôØËâ≤
                        RoundedCornerShape(6.dp)
                    )
                    .border(
                        1.dp,
                        JewelTheme.globalColors.borders.normal, // ‰ΩøÁî®‰∏ªÈ¢òÁöÑÊ≠£Â∏∏ËæπÊ°ÜËâ≤
                        RoundedCornerShape(6.dp)
                    )
                    .padding(4.dp)
            ) {
                models.forEach { model ->
                    Text(
                        text = model.displayName,
                        style = JewelTheme.defaultTextStyle.copy(
                            fontSize = 11.sp,
                            color = if (model == currentModel) 
                                JewelTheme.globalColors.text.normal 
                            else 
                                JewelTheme.globalColors.text.disabled
                        ),
                        modifier = Modifier
                            .clickable {
                                onModelChange(model)
                                expanded = false
                            }
                            .padding(horizontal = 8.dp, vertical = 4.dp)
                            .fillMaxWidth()
                    )
                }
            }
        }
    }
